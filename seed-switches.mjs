const existsBySlug = db.prepare('SELECT id FROM products WHERE slug = ?');
let added = 0, skipped = 0;
for (const p of products) {
  if (existsBySlug.get(p.slug)) { skipped++; continue; }
  const imgUrl = imageMap[p.slug];
  // Images are saved locally; record path under /uploads/<slug>.<ext>
  const images = imgUrl ? [`/uploads/${p.slug}${pickExt(imgUrl)}`] : [];
  createProduct({
    name: p.name,
    slug: p.slug,
    short_description: p.short,
    description: '',
    price: 0,
    sale_price: null,
    category_id: p.cat === 'switch' ? SWITCH_CAT : OTHERS_CAT,
    images,
    attributes: p.attrs,
    badge: '',
    featured: false,
    in_stock: true,
    brand: p.brand,
  });
  added++;
}

function pickExt(url) {
  const m = url.toLowerCase().match(/\.(jpe?g|png|webp|avif|gif)(?:[?#]|$)/);
  return m ? '.' + m[1].replace('jpeg', 'jpg') : '.jpg';
}

